/**
 * Renders a styled label for a dropdown menu.
 *
 * Applies standard padding and font styling; when `inset` is `true`, extra left padding is applied and a `data-inset` attribute is set.
 *
 * @param inset - If `true`, applies inset left padding to align with indented menu items
 * @returns A React element representing the dropdown menu label
 */
function DropdownMenuLabel({
  className,
  inset,
  ...props
}: React.ComponentProps<typeof DropdownMenuPrimitive.Label> & {
  inset?: boolean
}) {
  return (
    <DropdownMenuPrimitive.Label
      data-slot="dropdown-menu-label"
      data-inset={inset}
      className={cn(
        "px-2 py-1.5 text-sm font-medium data-[inset]:pl-8",
        className
      )}
      {...props}
    />
  )
}

